    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        
        try {
            this.fornecedor.salvar(this.jtfRazaoSocial.getText(), this.jtfNomeFantasia.getText() , this.jtfCnpj.getText(), this.jtfTelefone.getText(), this.jtfEmail.getText(), this.fornecedor.getFornecedor().getId());
        
            JOptionPane.showMessageDialog(rootPane, "Registro atualizado com sucesso");
            
            Fornecedor telaFornecedor = new Fornecedor();
            telaFornecedor.setVisible(true);
            
            this.dispose();
        } catch (BancoDeDadosException ex) {
            Logger.getLogger(EditarFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed

